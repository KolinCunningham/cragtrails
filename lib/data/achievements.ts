export function getLevelForXP(xp: number): XPLevel {
  // Walk from top to find the right level
  for (let i = XP_LEVELS.length - 1; i >= 0; i--) {
    if (xp >= XP_LEVELS[i].minXP) return XP_LEVELS[i];
  }
  return XP_LEVELS[0];
}

export function getXPProgress(xp: number): { level: XPLevel; progressPercent: number; xpIntoLevel: number; xpNeeded: number } {
  const level = getLevelForXP(xp);
  const xpIntoLevel = xp - level.minXP;
  const xpNeeded = level.maxXP === Infinity ? 0 : level.maxXP - level.minXP;
  const progressPercent = xpNeeded === 0 ? 100 : Math.min(100, (xpIntoLevel / xpNeeded) * 100);
  return { level, progressPercent, xpIntoLevel, xpNeeded };
}
